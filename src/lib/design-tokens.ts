// Helper functions
export const getTypographyClass = (variant: string) => {
  const variants: Record<string, string> = {
    'text-xs': 'text-xs font-normal leading-normal',
    'text-sm': 'text-sm font-normal leading-normal',
    'text-base': 'text-base font-normal leading-normal',
    'text-lg': 'text-lg font-normal leading-normal',
    'text-xl': 'text-xl font-semibold leading-tight',
    'text-2xl': 'text-2xl font-bold leading-tight',
    'text-3xl': 'text-3xl font-bold leading-tight',
    'text-4xl': 'text-4xl font-extrabold leading-tight',
  };
  return variants[variant] || variants['text-base'];
};

